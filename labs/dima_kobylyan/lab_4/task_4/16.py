"""4(16) Дано цілочисельний список , що не містить однакових чисел.Перевірити ,чи утворюють його \
елементи арифметичну прогресію (A,A+D,A+2*D,A+3*D...).Якщо утворюють , то вивести різницб прогресії,
якщо ні - вивести 0."""
lst = [5, 10, 15,20,5]
print(len(lst))
d = (lst[1]) - (lst[0])
if (lst[2]) % d == 0:
    print('Ваш список явл прогрессией', d, '- різниця прогрессії')
else:
    print(0)
