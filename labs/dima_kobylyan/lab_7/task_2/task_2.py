"""Описати рекурсивну функцію Fib1 (N) цілого типу, яка обчислює N-й елемент
послідовності чисел Фібоначчі (N - ціле число):
F1 = F2 = 1,
FK = FK-2 + FK-1, K = 3, 4, ....
За допомогою цієї функції знайти п'ять чисел Фібоначчі з даними номерами, і
вивести ці числа разом з кількістю рекурсивних викликів функції Fib1, що необхідні
для їх знаходження."""


def fibonacci(n):
    if n in (1, 2):
        return 1
    return fibonacci(n - 1) + fibonacci(n - 2)


print(fibonacci(7))