"""Описати рекурсив ну функцію DigitSum (K) цілого типу, яка знаходить суму цифр
цілого числа K, не використовуючи оператор циклу. За допомогою цієї функції
знайти суми чисел для п'яти даних цілих чисел."""


def DigitSum(K):
    if len(K) == 1:
        return K[0]
    else:
        return K[0] + DigitSum(K[1:])


print(DigitSum([1, 2, 3, 4, 5]))
