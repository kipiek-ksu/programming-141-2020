""" Дано додатні числа А і В (А>B).На відрізку довжиною А розміщено максимально можлива
 кількість відрізків довжиною В ( без накладання). Не використовуючи операції множення та ділення , знайти довжину
 незайнятої частини відрізка А.
"""
a = int(input('Введіть А: '))
b = int(input('Введить B: '))
while a > b:
    a = a - b
print(a)
