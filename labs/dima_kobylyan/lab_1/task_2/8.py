"""8. Відомо, що X кг цукерок коштують A гривень.
Визначити, скільки коштує 1 кг і Y кг цих же
цукерок."""
X = float(input(' Введите Кг конфет :  '))
A = float(input('Введите цену для конфет X :  '))
KG = A / X
print('Цена 1-го кг конфет', KG)
Y = float(input('Введите кг конфет , чтоб узнать цену : '))
cen = KG * Y
print('Цена конфет', cen)
