"""5. Дано ціле число. Вивести його рядок-опис виду «від'ємне парне число»,
 «нульове число», «додатне непарне число» і т. д."""

a = int(input('Введіть ціле число: '))

if not (a % 2) and a > 0:
    print('Число додатнє, парне')
elif (a % 2) != 0 and a > 0:
    print('Число додатнє, непарне')
elif a == 0:
    print('Число нульове')
elif not (a % 2) and a < 0:
    print("Число від'ємне, парне ")
elif (a % 2) != 0 and a < 0:
    print("Число від'ємне, непарне ")
else:
    print('Число невірне')
