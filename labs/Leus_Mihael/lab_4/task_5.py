
"""
17. Дано список A, кількість елементів якого N. Сформувати новий список B того ж розміру за таким
правилом: елемент BK дорівнює середньому арифметичному елементів масиву A з номерами від 1 до
к
"""

A = list(input('A = '))
N = len(A)
i = 0
Max = 0
B = list()
while i < N:
  Max += float(A[i])
  B.append(Max / (i + 1))
  i += 1

print (B)