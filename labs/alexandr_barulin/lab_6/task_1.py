"""
Описати функцію PowerA3 (A, B), яка обчислює третю степінь числа A і повертає її в змінній B
(A - вхідний, B - вихідний параметр; обидва параметри є дійсними).
За допомогою цієї функції знайти треті ступені п'яти даних чисел.
"""

def power_3(param: float) -> float:

    return param ** 3

print(power_3(float(input("Number= "))))
