"""
Підрахувати k - кількість тризначних натуральних чисел, сума цифр яких дорівнює n (1≤n≤27).
Операції ділення не використовувати.
"""


n = int(input("Сумма цифр равна:"))
c = 0
for i in range(1, 10):
    for j in range(0, 10):
        p = n-i-j
        if 0 <= p <= 9:
            c += 1
print("Таких чисел существует:" + str(c))
